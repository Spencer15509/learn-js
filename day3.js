//conditional statements

//if statements
let age = 18;
if (age >= 18){
   console.log("you can vote");
} //executes if the statement is true

//if-else statement
let score = 40;
if (score >= 40){
   console.log("pass");
}
else {
    console.log("fail");
}

//else if statement
let time = 8;
if (time < 12){
    console.log("morning");
}
else if(time < 17){
    console.log("afternoon");
}
else {
    console.log("evening");
}


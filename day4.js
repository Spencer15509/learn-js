//for loops-repeat a block of code a limited amount of times
for(let i = 1; i <= 20; i+= 2){
    console.log(i);
}
for(let n = 10; n > 0; n--){
    console.log(n);
}

for (h = 0; h <= 8; h++){
    console.log(h);
}
let sum = 0;
for (f = 0; f <= 9; f++){
    sum += f;
}
console.log(sum);